void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
	if (htim->Instance == TIM6) {
		rpm = (speed_pulse * 600) / 400;
		position = position_pulse % 400;
		speed_pulse = 0;
	}

}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin) {
	if (GPIO_Pin == a_signal_Pin) {
		speed_pulse++;
	}

	if (HAL_GPIO_ReadPin(b_signal_GPIO_Port, b_signal_Pin) == GPIO_PIN_SET) {
		direction = 1;
		position_pulse--;
	} else {
		direction = 0;
		position_pulse++;
	}

	if (GPIO_Pin == direction_change_Pin) {
		a = 1;
		if (HAL_GPIO_ReadPin(direction_GPIO_Port, direction_Pin) == GPIO_PIN_RESET)
			HAL_GPIO_WritePin(direction_GPIO_Port, direction_Pin, GPIO_PIN_SET);

		else {
			HAL_GPIO_WritePin(direction_GPIO_Port, direction_Pin,
					GPIO_PIN_RESET);
		}
	}
}

